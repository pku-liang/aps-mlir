// Globals
int GS = 0; // group size global for quantization of the weights

// Read RISC-V cycle counter (64-bit on both rv32 and rv64)
static inline unsigned long long read_cycle(void) {
#if __riscv_xlen == 32
    uint32_t lo, hi, hi2;
    do {
        asm volatile ("csrr %0, cycleh" : "=r" (hi));
        asm volatile ("csrr %0, cycle"  : "=r" (lo));
        asm volatile ("csrr %0, cycleh" : "=r" (hi2));
    } while (hi != hi2);
    return ((unsigned long long)hi << 32) | lo;
#else
    unsigned long long cycle;
    asm volatile ("csrr %0, cycle" : "=r" (cycle));
    return cycle;
#endif
}

// Read RISC-V instruction retired counter (64-bit on both rv32 and rv64)
static inline unsigned long long read_instret(void) {
#if __riscv_xlen == 32
    uint32_t lo, hi, hi2;
    do {
        asm volatile ("csrr %0, instreth" : "=r" (hi));
        asm volatile ("csrr %0, instret"  : "=r" (lo));
        asm volatile ("csrr %0, instreth" : "=r" (hi2));
    } while (hi != hi2);
    return ((unsigned long long)hi << 32) | lo;
#else
    unsigned long long instret;
    asm volatile ("csrr %0, instret" : "=r" (instret));
    return instret;
#endif
}

long time_in_ms() {
    // return time in milliseconds based on cycle counter and CPU frequency
    return (long)(read_cycle() / (CPU_FREQ_HZ / 1000));
}
